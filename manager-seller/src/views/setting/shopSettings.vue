<template>
  <div class="bg-settings">
    <el-form
      :model="shopDataForm"
      ref="shopDataForm"
      :rules="rules"
      label-width="200px"
      class="demo-ruleForm"
      status-icon>
      <!--身份证号-->
      <el-form-item label="身份证号：" prop="legal_id">
        <span>{{ shopDataForm.legal_id }}</span>
      </el-form-item>
      <!--店铺地址-->
      <el-form-item label="店铺地址：" prop="shop_address">
        <en-region-picker :api="areasapi" :default="areas" @changed="handleChange"></en-region-picker>
      </el-form-item>
      <!--详细地址-->
      <el-form-item label="详细地址：" prop="shop_add">
        <el-input v-model="shopDataForm.shop_add" style="width: 200px;" auto-complete="off"></el-input>
      </el-form-item>
      <!--联系电话-->
      <el-form-item label="联系电话：" prop="link_phone">
        <el-input v-model="shopDataForm.link_phone" style="width: 200px;" maxLength="11"></el-input>
      </el-form-item>
      <!--QQ-->
      <el-form-item label="QQ：" prop="shop_qq">
        <el-input v-model="shopDataForm.shop_qq" style="width: 200px;" placeholder="请设置店铺的客服QQ"></el-input>
        <span class="QQ-tip">（设置店铺的客服QQ）</span>
      </el-form-item>
      <!--店铺简介-->
      <el-form-item label="店铺简介：" prop="shop_desc">
        <UE ref="UE" :defaultMsg="shopDataForm.shop_desc" style="width: 80%;"></UE>
      </el-form-item>
      <!--店铺logo-->
      <el-form-item label="店铺logo：" prop="shop_logo">
        <el-upload
          class="upload-demo"
          key="shop_logo"
          :action="BASE_IMG_URL"
          :on-success="uploadSuccessLogo"
          :file-list="fileList_logo"
          ref="fileList_logo"
          list-type="picture">
          <el-button size="small" type="primary">
            上传<i class="el-icon-upload el-icon--right"></i>
          </el-button>
          <div slot="tip" class="el-upload__tip">此处为您的店铺logo，将显示在店铺Logo栏里。 （请上传200x60规格的图片！）</div>
        </el-upload>
      </el-form-item>
      <!--店铺banner-->
      <el-form-item label="店铺横幅：" prop="shop_banner">
        <el-upload
          class="upload-demo"
          key="shop_banner"
          :on-success="uploadSuccessBanner"
          :action="BASE_IMG_URL"
          :file-list="fileList_banner"
          ref="fileList_banner"
          list-type="picture">
          <el-button size="small" type="primary">上传<i class="el-icon-upload el-icon--right"></i></el-button>
          <div slot="tip" class="el-upload__tip">此处为您的店铺条幅，将显示在移动端。（请上传638x158规格的图片！） </div>
        </el-upload>
      </el-form-item>
      <el-form-item>
        <el-button type="primary" @click="handleSaveShopData('shopDataForm')">保存修改</el-button>
      </el-form-item>
    </el-form>
  </div>
</template>

<script>
  import * as API_Shop from '@/api/shop'
  import { UE } from '@/components'
  import { RegExp } from '～/ui-utils'
  export default {
    name: 'shopSetting',
    components: {
      [UE.name]: UE
    },
    data() {
      const validPhone = (rule, value, callback) => {
        if (!value) {
          callback(new Error('请输入联系人电话'))
        } else if (!RegExp.mobile.test(value)) {
          callback(new Error('请输入正确的11位手机号码'))
        } else {
          callback()
        }
      }
      return {
        /** 图片服务器地址 */
        BASE_IMG_URL: process.env.BASE_IMG_URL,

        areasapi: `${process.env.BASE_API}/regions/@id/children`,

        /** 店铺信息*/
        shopDataForm: {
          /** 店铺ID */
          shop_id: 0,

          /** 身份证号*/
          legal_id: '',

          /** 店铺地址id */
          shop_region: '',

          /** 详细地址*/
          shop_add: '',

          /** 联系电话*/
          link_phone: '',

          /** QQ*/
          shop_qq: '',

          /** 店铺简介*/
          shop_desc: '',

          /** 店铺logo*/
          shop_logo: '',

          /** 店铺横幅*/
          shop_banner: ''
        },

        /** 地区信息*/
        areas: [],

        /** 店铺logo图片存*/
        fileList_logo: [],

        /** 店铺banner图片存*/
        fileList_banner: [],

        /** 校验规则 校验必填 */
        rules: {
          /** 详细地址 */
          shop_add: [
            { required: true, message: '请填写详细地址', trigger: 'changer,blur' }
          ],
          /** 联系人电话 */
          link_phone: [
            { required: true, message: '请填写联系人电话', trigger: 'blur' },
            { validator: validPhone, trigger: 'blur' }
          ]
        }
      }
    },
    mounted() {
      this.GET_ShopGradeData()
    },
    methods: {
      /** 获取店铺信息 */
      GET_ShopGradeData() {
        API_Shop.getShopData().then(response => {
          this.shopDataForm = { ...response }
          this.fileList_logo = [{ url: this.shopDataForm.shop_logo }]
          this.fileList_banner = [{ url: this.shopDataForm.shop_banner }]
          this.areas = [this.shopDataForm.shop_province_id, this.shopDataForm.shop_city_id,
            this.shopDataForm.shop_county_id || -1, this.shopDataForm.shop_town_id || -1]
        })
      },

      /** 保存店铺设置*/
      handleSaveShopData(formName) {
        this.shopDataForm.shop_desc = this.$refs['UE'].getUEContent()
        this.$refs[formName].validate((valid) => {
          if (valid) {
            const _params = {
              shop_add: this.shopDataForm.shop_add,

              link_phone: this.shopDataForm.link_phone,

              shop_logo: this.shopDataForm.shop_logo,

              shop_banner: this.shopDataForm.shop_banner,

              shop_desc: this.shopDataForm.shop_desc || '',

              shop_qq: this.shopDataForm.shop_qq,

              shop_region: this.shopDataForm.shop_region
            }
            API_Shop.saveShopSettings(_params).then(() => {
              this.$message.success('保存店铺设置成功')
              this.GET_ShopGradeData()
            })
          }
        })
      },

      /** 操作地区选择器改变时 触发*/
      handleChange(val) {
        this.shopDataForm.shop_region = val.last_id
      },

      /** 上传logo成功以后*/
      uploadSuccessLogo(response) {
        this.fileList_logo.shift()
        this.fileList_logo.push(response)
        this.shopDataForm.shop_logo = response.url
      },

      /** 上传banner成功之后*/
      uploadSuccessBanner(response) {
        this.fileList_banner.shift()
        this.fileList_banner.push(response)
        this.shopDataForm.shop_banner = response.url
      }
    }
  }
</script>

